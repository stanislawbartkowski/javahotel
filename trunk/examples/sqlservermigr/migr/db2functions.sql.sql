CREATE OR REPLACE FUNCTION GETDATE()
RETURNS TIMESTAMP
RETURN CURRENT TIMESTAMP
@

CREATE OR REPLACE FUNCTION DBO.DATEADD_DD(IN NOD INT, IN DAT TIMESTAMP)
RETURNS TIMESTAMP
RETURN DAT + NOD DAYS
@

CREATE OR REPLACE FUNCTION DBO.DATEADD_MM(IN NOD INT, IN DAT TIMESTAMP)
RETURNS TIMESTAMP
RETURN DAT + NOD MONTHS
@

CREATE OR REPLACE FUNCTION DBO.DATEADD_MI(IN NOD INT, IN DAT TIMESTAMP)
RETURNS TIMESTAMP
RETURN DAT + NOD MINUTES
@

CREATE OR REPLACE FUNCTION DBO.DATEDIFF_DD(IN STARTDATE TIMESTAMP, IN ENDDATE TIMESTAMP)
RETURNS INT
RETURN TIMESTAMPDIFF(16,CHAR(ENDDATE -STARTDATE))
@

CREATE OR REPLACE FUNCTION DBO.DATEDIFF_MM(IN STARTDATE TIMESTAMP, IN ENDDATE TIMESTAMP)
RETURNS INT
RETURN TIMESTAMPDIFF(64,CHAR(ENDDATE -STARTDATE))
@

CREATE OR REPLACE FUNCTION DBO.DATEDIFF_MI(IN STARTDATE TIMESTAMP, IN ENDDATE TIMESTAMP)
RETURNS INT
RETURN TIMESTAMPDIFF(4,CHAR(ENDDATE -STARTDATE))
@

