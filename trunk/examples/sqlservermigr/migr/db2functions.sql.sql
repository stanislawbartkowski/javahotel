CREATE OR REPLACE FUNCTION DBO.DATEPART_SS(IN da TIMESTAMP) 
RETURNS INT
  SOURCE SECOND(TIMESTAMP)
@

CREATE OR REPLACE FUNCTION DBO.DATEPART_MI(IN da TIMESTAMP) 
RETURNS INT
  SOURCE MINUTE(TIMESTAMP)
@

CREATE OR REPLACE FUNCTION ISNULL(IN S1 VARCHAR(32672),IN S2 VARCHAR(32672)) 
RETURNS VARCHAR(32672)
  RETURN COALESCE(S1,S2)
@

CREATE OR REPLACE FUNCTION CHARINDEX(VARCHAR(32672),VARCHAR(32672))
RETURNS INTEGER
SOURCE LOCATE(VARCHAR(32672), VARCHAR(32672))
@  

CREATE OR REPLACE FUNCTION SUBSTRING (IN STR VARCHAR(32672),IN STA INT, IN LEN INT)
RETURNS VARCHAR(32672)
  RETURN SUBSTRING(STR,STA,LEN,CODEUNITS16)
@

